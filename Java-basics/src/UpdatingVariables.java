public class UpdatingVariables {
    public static void main(String[] args) {
        // Integer
        int salary = 1000;

        salary += 200;

        salary -=  50;

        salary += (30 * 2) - 45;

        System.out.println(salary);

        // String
        String employeeName = "Fonzi";
        employeeName += " Lord";
        System.out.println(employeeName);
    }
}
